class PapyrusHelpers:
    """Generate and export Papyrus script templates."""

    @staticmethod
    def generate(template_id: str, script_name: str) -> tuple[bool, str]:
        """Return (True, script_text) for the given template and script name.

        ``script_name`` must be a valid Papyrus identifier (letters, digits,
        underscores).  Spaces are replaced with underscores automatically.
        """
        safe_name = script_name.replace(" ", "_").strip()
        if not safe_name:
            return False, "Script name cannot be empty"

        fn = _TEMPLATES.get(template_id)
        if fn is None:
            return False, f"Unknown template type: {template_id!r}"

        try:
            text = fn(safe_name)
            return True, text
        except Exception as exc:
            return False, f"Template generation failed: {exc}"

    @staticmethod
    def export(template_id: str, script_name: str,
               output_dir: str) -> tuple[bool, str]:
        """Write the script to ``<output_dir>/<ScriptName>.psc``.

        Creates ``output_dir`` if it does not exist.
        """
        ok, content = PapyrusHelpers.generate(template_id, script_name)
        if not ok:
            return False, content

        safe_name = script_name.replace(" ", "_").strip()
        try:
            os.makedirs(output_dir, exist_ok=True)
            filepath = os.path.join(output_dir, f"{safe_name}.psc")
            with open(filepath, "w", encoding="utf-8") as fh:
                fh.write(content)
            return True, f"Script saved: {filepath}"
        except Exception as exc:
            return False, f"Failed to write script: {exc}"

    @staticmethod
    def get_compile_instructions(script_name: str, mod_name: str = "MyMod") -> str:
        """Return a plain-text guide for compiling a .psc file."""
        return f"""Papyrus Compile Instructions for {script_name}.psc
=======================================================

1. In the Creation Kit:
   File → Preferences → Papyrus
   Set 'Source Folder' to your mod's Scripts\\Source\\ folder.

2. Place {script_name}.psc in:
   <FO4 Data>\\Scripts\\Source\\User\\{script_name}.psc

3. Compile via Creation Kit:
   Gameplay → Papyrus Script Manager → right-click → Compile

   OR from command line (PapyrusCompiler.exe from CK install):
   PapyrusCompiler.exe "{script_name}.psc" -f="Institute_Papyrus_Flags.flg" \\
       -i="Data\\Scripts\\Source\\User;Data\\Scripts\\Source\\Base" \\
       -o="Data\\Scripts"

4. The compiled .pex goes to:
   <FO4 Data>\\Scripts\\{script_name}.pex

5. In the Creation Kit, attach {script_name} to your form:
   Open the form → Scripts tab → Add → type '{script_name}'

Documentation: https://falloutck.uesp.net/wiki/Papyrus_Documentation
"""


