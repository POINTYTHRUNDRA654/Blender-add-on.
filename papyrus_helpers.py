"""
Papyrus Script Template Generator
===================================
Generates complete, ready-to-compile Fallout 4 Papyrus scripts for the most
common mod patterns.  Every template produces a valid ``.psc`` source file
with:

- Correct ``ScriptName … extends …`` header
- The standard event handlers for that script type (OnInit, OnActivate, etc.)
- Inline comments explaining every block
- Import statements for the most-used FO4 utility scripts
- Placeholder variables and properties that the modder fills in

Supported template types
------------------------
``OBJECT``       – Generic ObjectReference (catch-all for placed objects)
``WEAPON``       – Weapon script (equip, fire, unequip events)
``ARMOR``        – Armor / apparel (equip, unequip)
``ACTIVATOR``    – Activator trigger (OnActivate, proximity)
``CONTAINER``    – Container (open, close, item add / remove)
``DOOR``         – Door (lock/unlock, teleport marker)
``QUEST``        – Quest controller (stage progression, aliases)
``MAGIC_EFFECT`` – Magic effect / spell script
``ALIAS_ACTOR``  – Actor alias (for quests)
``ALIAS_REF``    – Reference alias (for quests)
``TERMINAL``     – Terminal menu (pages, entries, keywords)
``HOLOTAPE``     – Holotape game / recording
``WORKSHOP``     – Settlement / workshop placeable object
``NPC``          – NPC AI package / dialogue trigger

Usage
-----
>>> from papyrus_helpers import PapyrusHelpers
>>> ok, text = PapyrusHelpers.generate("QUEST", "MyMod_QuestController")
>>> ok2, msg = PapyrusHelpers.export("QUEST", "MyMod_QuestController",
...                                   "/path/to/Scripts/Source/")
"""

