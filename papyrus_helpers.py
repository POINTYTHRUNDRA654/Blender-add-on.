def register():
    bpy.types.Scene.fo4_papyrus_template    = bpy.props.EnumProperty(
        name="Script Template",
        description="Papyrus script type to generate",
        items=TEMPLATE_ENUM_ITEMS,
        default="OBJECT",
    )
    bpy.types.Scene.fo4_papyrus_script_name = bpy.props.StringProperty(
        name="Script Name",
        description="CamelCase name for the Papyrus script (no spaces)",
        default="MyMod_MyScript",
    )
    bpy.types.Scene.fo4_papyrus_output_dir  = bpy.props.StringProperty(
        name="Output Folder",
        description="Where to save the .psc file (e.g. Data/Scripts/Source/User)",
        subtype="DIR_PATH",
        default="",
    )
    bpy.types.Scene.fo4_papyrus_mod_name    = bpy.props.StringProperty(
        name="Mod Name",
        description="Your mod's prefix (used in compile instructions)",
        default="MyMod",
    )


def unregister():
    for prop in ("fo4_papyrus_template", "fo4_papyrus_script_name",
                 "fo4_papyrus_output_dir", "fo4_papyrus_mod_name"):
        if hasattr(bpy.types.Scene, prop):
            try:
                delattr(bpy.types.Scene, prop)
            except Exception:
                pass
